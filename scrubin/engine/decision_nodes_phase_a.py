# ---------------------------------------------------------------------------
# Helper factories for common empty structures
# ---------------------------------------------------------------------------
def _empty_failure() -> FailurePropagation:
    return FailurePropagation(
        latent_failure_score=0.0,
        cumulative_risk_delta=0.0,
        instability_contribution=0.0,
        contamination_contribution=0.0,
        hemorrhage_contribution=0.0,
        cognitive_overload_contribution=0.0,
        delayed_decompensation_ticks=0,
    )

def _noop_procedure_mutation() -> ProcedureStateMutation:
    return ProcedureStateMutation(
        unlock_phases=[],
        block_phases=[],
        advance_phase=False,
        repeat_phase=False,
        force_branch=None,
        terminate_procedure=False,
        require_consult=None,
    )

def _noop_resource_impact() -> ResourceImpact:
    return ResourceImpact()

def _instant_profile() -> ExecutionProfile:
    return ExecutionProfile(
        execution_type="instant",
        duration_seconds=0,
        interruptible=False,
        requires_stability=False,
        requires_visualization=False,
        requires_anesthesia_depth=False,
        requires_assistant=False,
    )

