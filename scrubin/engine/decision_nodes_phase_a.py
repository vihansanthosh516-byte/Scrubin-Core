"""Phase A (Pre‑Operative Evaluation) DecisionNode definitions.

All nodes use hierarchical IDs of the form
`appendectomy.<phase>.<category>.<action>`.
They are registered in :class:`scrubin.engine.decision_registry.DecisionRegistry`
so that the orchestrator (or an AI agent) can resolve a click to a concrete
``DecisionNode`` and call ``node.execute(world, rng)``.
"""

